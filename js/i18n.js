let currentLang = localStorage.getItem('lang') || 'en';

/** Translate a single key using current language */
function t(key) {
  const dict = I18N[currentLang] || I18N.en;
  const v = dict[key];
  if (v !== undefined && v !== null) return v;
  const fb = I18N.en && I18N.en[key];
  if (fb !== undefined && fb !== null) return fb;
  return key;
}

function applyI18n(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang === 'ko' ? 'ko' : 'en';
  const dict = I18N[lang] || I18N.en;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!dict[key]) return;
    if (el instanceof SVGElement) {
      /* SVG <text> — use innerHTML so <tspan> subscripts survive */
      el.innerHTML = dict[key];
    } else {
      el.innerHTML = dict[key];
    }
  });
  if (typeof renderMathInElement === 'function') {
    renderMathInElement(document.body, { delimiters: [{ left: '$$', right: '$$', display: true }, { left: '$', right: '$', display: false }] });
  }
  /* Re-render game log if results exist */
  if (typeof renderLog === 'function') renderLog();
  /* Re-render charts and game canvas so t()-based labels update */
  if (typeof fullRedraw === 'function') fullRedraw();
  else if (typeof redrawAll === 'function' && typeof LA !== 'undefined') redrawAll(LA, LR);
}
